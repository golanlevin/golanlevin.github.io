const LOCALE_ALIASES = {
  "zh-tw": "zh-hant",
  "zh-hk": "zh-hant",
  "zh-mo": "zh-hant",
  "no": "nb",
  "no-no": "nb",
};

/**
 * Normalize locale tags into the keys used by `LOCALES`.
 *
 * This keeps query-string overrides and browser-language detection aligned, especially for script-
 * or region-specific aliases such as Traditional Chinese (`zh-TW` -> `zh-hant`).
 *
 * @param {string} locale
 * @returns {string}
 */
function normalizeLocaleTag(locale) {
  const normalized = String(locale || "").trim().toLowerCase();
  if (!normalized) return "";
  return LOCALE_ALIASES[normalized] || normalized;
}

/**
 * Pick the startup locale, with `?lang=...` taking precedence over browser preferences.
 *
 * Regional tags such as `fr-CA` or `it-IT` collapse to their base locale when supported.
 *
 * @returns {string}
 */
function detectInitialLocale() {
  try {
    const params = new URLSearchParams(globalThis.location?.search || "");
    const requested = normalizeLocaleTag(params.get("lang"));
    if (requested && LOCALES[requested]) return requested;
  } catch {
    // Ignore environments without window/location.
  }
  try {
    const preferredLanguages = Array.isArray(globalThis.navigator?.languages) && globalThis.navigator.languages.length
      ? globalThis.navigator.languages
      : [globalThis.navigator?.language].filter(Boolean);
    for (const language of preferredLanguages) {
      const normalized = normalizeLocaleTag(language);
      if (!normalized) continue;
      const base = normalizeLocaleTag(normalized.split("-")[0]);
      if (normalized.startsWith("zh-hant") && LOCALES["zh-hant"]) return "zh-hant";
      if (LOCALES[normalized]) return normalized;
      if (LOCALES[base]) return base;
    }
  } catch {
    // Ignore environments without navigator.
  }
  return "en";
}

