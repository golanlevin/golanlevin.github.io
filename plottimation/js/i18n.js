/**
 * Resolve a dotted translation key against the active locale, falling back to English when the
 * current locale does not define that key.
 *
 * @param {string} path
 * @returns {*}
 */
function resolveKey(path) {
  const read = (locale) => path.split(".").reduce((value, part) => value?.[part], locale);
  return read(LOCALES[currentLocale]) ?? read(LOCALES.en);
}

/**
 * Translate a dotted locale key and apply simple `{placeholder}` interpolation.
 *
 * @param {string} path
 * @param {Record<string, string | number>} [vars={}]
 * @returns {string}
 */
export function t(path, vars = {}) {
  const value = resolveKey(path);
  if (typeof value !== "string") return path;
  return value.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
}

/**
 * Return the currently active locale key.
 *
 * @returns {string}
 */
export function getLocale() {
  return currentLocale;
}

/**
 * Change the active locale if it exists in the locale table.
 *
 * Callers should invoke `applyTranslations(...)` afterward to refresh already-rendered markup.
 *
 * @param {string} locale
 * @returns {void}
 */
export function setLocale(locale) {
  const normalized = normalizeLocaleTag(locale);
  if (LOCALES[normalized]) currentLocale = normalized;
}

/**
 * Translate markup annotated with `data-i18n*` attributes.
 *
 * Static HTML strings flow through this helper, while more dynamic runtime strings use `t(...)`
 * directly in the relevant controller modules.
 *
 * @param {Document | Element} [root=document]
 * @returns {void}
 */
export function applyTranslations(root = document) {
  root.documentElement?.setAttribute?.("lang", currentLocale);
  root.querySelectorAll("[data-app-version]").forEach((element) => {
    element.textContent = APP_VERSION;
  });
  for (const binding of TRANSLATION_BINDINGS) {
    root.querySelectorAll(binding.selector).forEach((element) => {
      const key = binding.getKey(element);
      if (!key) return;
      binding.apply(element, t(key));
    });
  }
}

/**
 * Resolve one tooltip key or a prioritized fallback list against the locale tooltip dictionary.
 *
 * @param {Record<string, string>} tooltips
 * @param {string | string[]} keys
 * @returns {string}
 */
function resolveTooltipValue(tooltips, keys) {
  const keyList = Array.isArray(keys) ? keys : [keys];
  for (const key of keyList) {
    const value = tooltips[key];
    if (typeof value === "string" && value) return value;
  }
  return "";
}

/**
 * Build the selector-keyed tooltip lookup used by the legacy tooltip wiring.
 *
 * The tooltip system still attaches strings by CSS selector, so this adapter keeps the locale
 * dictionaries decoupled from that older interface.
 *
 * @returns {Record<string, string>}
 */
export function getTooltipText() {
  const tooltips = resolveKey("tooltip") || {};
  return Object.fromEntries(
    Object.entries(TOOLTIP_SELECTOR_KEYS).map(([selector, keys]) => [
      selector,
      resolveTooltipValue(tooltips, keys),
    ]),
  );
}
